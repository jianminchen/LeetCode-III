class NestedIterator(object):

    def __init__(self, nestedList):
        """
        Initialize your data structure here.
        :type nestedList: List[NestedInteger]
        """
        self.i = nestedList
        self.stack = []

    def next(self):
        """
        :rtype: int
        """
        return self.stack.pop()

    def hasNext(self):
        """
        :rtype: bool
        """
        while self.i or self.stack:
            if not self.stack:
                self.stack += [self.i.pop(0)]
            else:
                if self.stack[-1].isInteger():
                    return True
                else:
                    while self.stack and not self.stack[-1].isInteger():
                        temp = self.stack.pop().getList()[::-1]
                        for i in temp:
                            self.stack += [i]
        return False
                        

